package veriableanddatatypes;

public class thirdjavaclass {

	public static void main(String[] args)
	{
		
		int a=10;
		int b=20;
//		swap two variables
		
		System.out.println("A & B Value Before");
		System.out.println("A="+ a);
		System.out.println("B="+ b);
		System.out.println(); 
		
		a=a+b;
		b=a-b;
		a=a-b;
		
		System.out.println("A & B Value After");
		System.out.println("A=" + a);
		System.out.println("B=" + b);
		
		
	}
	
}


